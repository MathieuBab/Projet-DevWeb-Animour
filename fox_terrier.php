    <h1><i class="fa fa-paw"></i> Le Fox Terrier <i class="fa fa-paw"></i></h1><br>
    
    <h2><i class="fa fa-paw"></i> Informations sur la race</h2>
    <p class="infos">
        Le Fox Terrier est un petit chien plein d'énergie et de force, malgré ses dimensions réduites. 
        Il est harmonieusement construit et dégage une impression de grande fierté, notamment lorsqu'il est en position debout. 
        Sa stature évoque alors celle du cheval de chasse. 
        On distingue 2 variétés de Fox Terrier : celle à poil lisse (standard FCI N°12) et celle à poil dur (standard FCI N°169). 
        Cette dernière est reconnaissable à la barbe et la moustache que porte le chien.
    </p>
    <p class="infos">
        Il possède un type de poil court. 
        La femelle peut atteindre une taille de 33cm à 35cm pour 6kg à 8kg. 
        La taille du mâle peut atteindre 35cm à 39cm pour 7kg à 9kg.
    </p>
    <br>
    
    <h2><i class="fa fa-paw"></i> Historique de la race</h2>
    <p class="infos">
        D'origine britannique, le Fox Terrier est l'une des plus anciennes races de terriers. 
        Elle descend probablement du même type de chien ayant engendré le Manchester Terrier et le Bull Terrier. 
        Le premier standard de la race a été rédigé en 1876. 
        Si le Fox Terrier est aujourd'hui un excellent chien d'exposition et de compagnie, c'est surtout pour la chasse que la race a été développée.
    </p>
    <p class="infos">
        Comme son nom l'indique, sa spécialité est de débusquer le renard qui se cache dans son terrier afin de permettre aux chasseurs de poursuivre leur tâche. 
        Il est également doué pour déloger les rongeurs et toutes sortes de nuisibles. 
        Les qualités recherchées pour le travail sont donc la vitesse, la petite taille, la détermination et le courage. 
        La race du Fox Terrier à poil lisse a été définitivement reconnue par la FCI en juin 1993, tandis que celle à poil dur l'a été en mai 1955.
    </p>
    <br>

    <h2><i class="fa fa-paw"></i>  Particularités physiques</h2>
    <p class="infos">
        Son poil : lisse, droit, plat et dense chez le Fox Terrier à poil lisse. Dur et dense chez le Fox Terrier à poil dur, avec une longueur de 2 cm sur les épaules et de 4 cm sur le dos, le garrot, l'arrière-main et les côtes. Chez la variété à poil dur, toujours, la texture est rêche au niveau des mâchoires et des membres.
        Sa couleur : majoritairement blanche avec des marques feu et/ou noires, chez les 2 variétés.
        Sa tête : crâne plat et dont la largeur s'amenuise à mesure que l'on se rapproche des yeux. Le stop est très légèrement marqué, la truffe est noire et les mâchoires sont dotées d'une forte musculature.
        Ses oreilles : en forme de V, de petite taille, tombant en avant et contre les joues.
        Ses yeux : de couleur foncée, de forme arrondie, bien enfoncés dans l'orbite et affichant une expression de vitalité et d'intelligence.
        Son corps : dos court, horizontal et solide. Le rein est puissant, la poitrine bien descendue sans être large, les côtes arrière légèrement cintrées et celles postérieures de bonne longueur.
        Sa queue : forte, attachée haut, portée droite et gaiement, jamais enroulée. Etait écourtée par le passé.
    </p>
