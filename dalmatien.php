        Son corps : le garrot est bien dessiné, le dos est puissant et droit, le rein court et musclé, la croupe inclinée à moins de 30°.
        Sa queue : forte à l’attache, puis s'affine en arrivant vers l’extrémité. Portée en sabre et attachée en continuité avec la croupe.
