    <h2>Particularités physiques</h2>
    <p class="infos">
        Son poil : court, luisant, dur et dense, et ce, sur l'ensemble de la robe du chien.
        Sa couleur : fond d'un blanc pur avec des taches rondes de 2 à 3 cm de diamètre, de couleur noire ou marron, selon que le chien appartienne à l'une ou l'autre des deux variétés. Ces taches doivent être symétriquement réparties sur tout le corps et nettement délimitées par rapport au fond blanc.
        Sa tête : en forme de prisme, en proportion et en harmonie avec le reste du corps. Le crâne est plat, avec un léger arrondi latéral. Le stop est modérément marqué.
        Ses oreilles : en forme de triangles isocèles, avec les extrémités arrondies et des taches noires ou marron semblables à celles de la robe. Attachées haut et portées bien contre la tête. 
        Son corps : le garrot est bien dessiné, le dos est puissant et droit, le rein court et musclé, la croupe inclinée à moins de 30°.
        Sa queue : forte à l’attache, puis s'affine en arrivant vers l’extrémité. Portée en sabre et attachée en continuité avec la croupe.
    </p>
