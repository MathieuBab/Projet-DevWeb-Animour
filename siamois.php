    <h2><i class="fa fa-paw"></i> Historique de la race</h2>
    <p class="infos">
        Les origines du Siamois remontent au 15e siècle, voire même au 14e siècle, si l’on en croit les manuscrits datant de cette époque et découverts à Ayutthaya, capitale du royaume de Siam (l’actuelle Thaïlande). 
        Le zoologiste allemand Peter Simon Pallas (1741-1811) évoquait également des chats aux caractéristiques semblables à celles du Siamois, présents en Asie durant cette période.
    </p>
    <p class="infos">
        2 chats encore inconnus à l’époque, possédant l’aspect du Siamois, ont été présentés lors de la Grande Exposition au Crystal Palace de Londres en 1871. 
        Durant l’édition de 1885, un couple de Siamois, prénommés Pho et Mia, attiraient l’attention des visiteurs. 
        Ils appartenaient à la sœur du Major Gould, consul d’Angleterre à Bangkok, ce dernier les ayant ramenés de Thaïlande pour les lui offrir.
    </p>

    <p class="infos">
        Reconnue 4 années plus tard, la race a commencé à gagner en popularité dans l’entre-deux-guerres. 
        Les premiers Siamois de l’époque étaient principalement des seal point, mais des chats chocolat, blue et lilac point ont également commencé à être sélectionnés.
    </p>

    <p class="infos">
        De part et d’autre de l’Atlantique, des développements légèrement différents de la race ont vu le jour. 
        Si, chez les éleveurs aux Etats-Unis, on priorisait l’élégance du corps et la longueur de la tête, chez ceux anglais, on privilégiait plutôt des oreilles attachées plus bas et de plus grande taille, ainsi qu’une forme d’œil orientale.
    </p>

    <p class="infos">
        Plusieurs autres races de chats ont ensuite été créées à partir du Siamois : le Balinais, l’Himalayen, le Havana, l’Oriental ou encore le Mandarin.
        La race du Siamois a été reconnue par la Fédération Internationale Féline (FIFé) en 1945.
    </p>
    <br>

    <h2><i class="fa fa-paw"></i> Particularités physiques</h2>
    <p class="infos">
        Son corps : élégant, à l’ossature fine et à la musculature bien développée. Le cou est relativement long et fin, les pattes longues, les pieds fins et de forme ovale.
        Son poil : court, brillant, bien couché sur le corps, soyeux au toucher, sans sous-poil.
        Sa couleur : toutes les couleurs d’extrémités (colourpoint) que sont le masque, les oreilles, les pattes et la queue, contrastant avec celle du corps, sont admises.
        Sa tête : cunéiforme, elle est triangulaire lorsque vue de face. Le museau est fin, la ligne de profil droite et le menton bien marqué.
        Ses yeux : en amande et d’un bleu intense. Ils confèrent au chat un regard typiquement oriental.
        Ses oreilles : de grande taille, larges à leur attache, s’inscrivant dans la forme triangulaire de la tête.
        Sa queue : fine et allongée, elle est dans la continuité de la conformation longue du corps. Elle se termine en pointe.
    </p>
