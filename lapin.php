    <p class="infos">
        Si le Lapin est gardé en cage, il doit pouvoir en sortir suffisamment longtemps (plusieurs heures) au quotidien pour bouger librement et explorer son environnement. 
        C’est surtout en début de soirée qu’il convient de le relâcher. 
        Il est recommandé de le placer dans un endroit dépourvu de source de stress, comme les bruits et les mouvements brusques. 
        La cage doit être constamment ouverte pour permettre à votre Lapin d’accéder au monde extérieur en permanence. 
        Elle doit être spacieuse, avec une longueur d’environ 1,80 mètre et d’une largeur de 60 cm. 
        Privilégiez un matériau en métal, comme une cage à barreaux, ou en plexiglas à condition que la cage soit suffisamment aérée. 
        Par la suite, le Lapin devra retrouver plusieurs accessoires indispensables à son bien-être et à sa vie quotidienne : des gamelles de nourriture et d’eau, ou un biberon, un râtelier pour son foin, une cachette et bien évidemment un bac à litière.
