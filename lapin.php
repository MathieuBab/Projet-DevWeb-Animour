    <h2><i class="fa fa-paw"></i> Informations</h2>
    <p class="infos">
        Les lapins sont d'adorables boules de poils. 
        Ils ont un caractère doux pour la plupart, qu'il s'agisse de la grande race, de celle considérée comme moyenne, de la plus petite ou des Lapins nains. 
        Le lapin fait partie de l'ordre des lagomorphes. 
        Très rarement agressif, il peut surtout être peureux lors des premiers moments passés en votre compagnie. 
        Il convient dès lors de vous montrer rassurant, de le comprendre et de l'apprivoiser avec délicatesse. 
        Animal diurne, il appréciera par la suite être câliné sur son frond (jamais sur son dos). 
        En permanence, vous devez vous montrer bienveillant avec lui.
    </p>
    <p class="infos">
        Les lapins peuvent atteindre une taille de 21cm à 45cm pour 800g à 6kg. 
