    <h2><i class="fa fa-paw"></i> Historique</h2>
    <p class="infos">
        La domestication du Lapin a débuté vers le 15e siècle à partir d'une espèce en particulier, le Lapin de garenne (Oryctolagus cuniculus). 
        Toutes les variétés élevées de nos jours en descendent et leurs différences (tailles, couleurs...) résultent de sélections opérées par l'Homme. 
        A l'origine, la plupart des lapins de grande taille étaient élevés pour leur chair, mais ils sont aujourd'hui autant appréciés comme animaux de compagnie que les autres variétés (races moyennes, petites et naines).
    </p>
    <br>

    <h2><i class="fa fa-paw"></i> Particularités physiques</h2>
    <p class="infos">
        Moins élancé que le Lièvre, le Lapin est reconnaissable à ses longues oreilles et à son corps compact. 
        Il est doté de 28 dents qui poussent de manière permanente et est capable de courir à une vitesse pouvant atteindre les 40 km/h.
    </p>

    <p class="infos">
        Son corps : compact et de forme cylindrique, conique ou convexe.
        Son poil : court ou long (dit « angora) en fonction des variétés.
        Sa couleur : variée, unie ou avec marques, allant du blanc au noir, en passant par le feu, le bleu et le gris.
        Sa tête : dotée de vibrisses (poils tactiles). La lèvre supérieure est clairement séparée en 2 pour constituer le bec de lièvre. Le front est large, le chanfrein présentant une courbure plus ou moins marquée, présence de fanon chez certaines femelles.
        Ses yeux : disposés de part et d’autre de la tête, dotés de 3 paupières.
        Ses oreilles : longues, placées vers l’arrière, souvent dressées (sauf chez le Lapin Bélier notamment).
        Sa queue : courte ou moyenne, plaquée contre le corps.
    </p>
