    <h2><i class="fa fa-paw"></i> Santé</h2>

    <p class="infos">Solide</p>
    <div class="box"> 
        <div class="skill soixante">60%</div>
    </div><br>

    <p class="infos">
        Il est recommandé de surveiller la température corporelle du Lapin, qui doit être comprise entre 38,5 et 39,5°C. 
        Il convient également de s’assurer que la pousse des dents soit normale. 
        Autrement, si une ou plusieurs dents poussent de travers, cela peut avoir des conséquences sur son alimentation et donc sa santé. 
        Côté vaccins, le Lapin peut être protégé contre la myxomatose et la maladie virale hémorragique, 2 affections dangereuses pour cet animal.
    </p><br>


    <h2><i class="fa fa-paw"></i> Espérance de vie</h2>

    <p class="infos">
        L'espérance de vie d'un lapin se situe, en moyenne, entre 7 ans et 12 ans. 
    </p><br>

    <h2><i class="fa fa-paw"></i> Entretien et hygiène</h2>

    <p class="infos">
        Il convient de vérifier très régulièrement plusieurs parties du corps du Lapin : ses yeux, ses oreilles et ses dents. 
        En effet, une mauvaise dentition aura une forte incidence sur sa façon de se nourrir. 
        Il mange constamment du foin. Il est donc important d’éviter toute malocclusion dentaire. 
        Il convient également de brosser régulièrement le Lapin, afin de lui laisser ses poils soyeux et brillant, et d’éviter tout dépôt de parasites. 
        Enfin, parfois, il faut également tailler ses griffes. 
        Pour ce qui est de l’hygiène du Lapin, il apprendra à faire ses besoins dans sa litière sur le tas. 
        Dans un premier temps, lors de son apprivoisement, il faut que le propriétaire montre avec des gestes répétés que les crottes du Lapin doivent être mises dans son bac à litière. 
        A force, il ira de lui-même faire ses besoins dedans. 
        Par ailleurs, le Lapin effectue sa toilette quotidienne, notamment en journée, lorsqu’il alterne entre phase d’éveil et sommeil.
    </p><br>
    

