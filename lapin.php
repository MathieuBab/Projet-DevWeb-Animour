    <h2><i class="fa fa-paw"></i> Reproduction</h2>

    <p class="infos">
        La maturité sexuelle du Lapin dépend de sa taille. 
        Elle est assez précoce chez le lapin nain alors qu’elle est tardive chez les lapins de grande race. 
        Elle varie donc entre 4 mois à un an pour les plus grands. 
        Cependant, il convient de ne pas faire accoupler votre femelle trop hâtivement, car elle n’a pas fini sa croissance avant une dizaine de mois. 
        Ensuite, elle peut reproduire durant toute l’année, sans période prédéfinie. La période de gestation se situe entre 4 et 5 semaines. 
        Il n’est pas conseillé de faire subir plus de 4 portées par an à la Lapine, qui pourrait se trouver fatiguée. 
        Durant sa période de gestation, la Lapine a besoin d’un régime alimentaire plus riche, notamment lors de la dernière semaine avec un ajout sensible en calcium et vitamines. 
        Les bébés lapins, une fois nés, n’ont pas de poils et pèsent entre 30 et 80 grammes. 
        Leur nombre varie de 4 à 12 par portée, même s’il est souvent moindre lors d’une première portée. 
        Il ne faut pas stresser la Lapine pendant qu’elle s’occupe de ses petits. 
        Un trop fort stress pourrait l’entraîner à manger l’un de ses nouveaux-nés. 
        Le sevrage a lieu au bout de la troisième semaine.
    </p><br>


