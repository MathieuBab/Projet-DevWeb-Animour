    <h2><i class="fa fa-paw"></i> Historique de la race</h2>
    <p class="infos">
        Le Cocker Anglais est l'une des plus anciennes races de spaniels britanniques. 
        Elle a été reconnue en tant que race à part entière en 1873, année de la création du Kennel Club. 
        Son origine est assez floue même s’il est acquis depuis toujours que ce chien provient d’Angleterre. 
        Chasseur-né, le Cocker Anglais s'était particulièrement illustré comme leveur de bécasses. 
        Son nom "Cocker" vient d'ailleurs du mot anglais "woodcock" qui veut dire bécasse. 
        Paul Gaillard l’introduit en France à la fin du 19e siècle. Le standard de la race a été fixé en 1901.
    </p>
