        Sa tête : l’Exotic Shorthaire présente une tête ronde et assez massive. Le crâne, large, forme un dôme. Il a des joues puissantes et larges, ainsi que des pommettes pleines. Son nez est large et dispose de narines assez ouvertes. Le stop est bien marqué. Il est placé entre les yeux. Le museau est rond, large et plein.  
